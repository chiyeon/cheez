const piece_score = (piece, x, y) => {
    if (!piece) return 0
    let score = piece_values[piece.type] + position_tables[piece.color][piece.type][y][x]
    
    return piece.color == "w" ? score : -score;
}

// evaluates the position where positive = white favor 
const evaluate_position = (board) => {
    let score = 0

    for (let x = 0; x < 8; x++) {
        for (let y = 0; y < 8; y++) {
            score += piece_score(board[x][y], x, y)
        }
    }

    return score;
}

const start_minimax = (start_depth, game, player_sided) => {
    let best_move_eval = -9999
    let best_move = null

    let game_moves = game.moves()

    for (let i = 0; i < game_moves.length; i++) {
        let move = game_moves[i]

        game.move(move)

        let eval = minimax(start_depth - 1, game, -10000, 10000, player_sided)

        game.undo()

        if (eval >= best_move_eval) {
            best_move = move
            best_move_eval = eval
        }
    }

    return best_move
}

const minimax = (depth, game, alpha, beta, player_sided) => {
    positions_evaluated++

    if (depth == 0) return (player_sided ? 1 : -1) * evaluate_position(game.board())

    let game_moves = game.moves()

    if (player_sided) {
        let best_move_eval = -9999

        for (let i = 0; i < game_moves.length; i++)
        {
            let move = game_moves[i]

            game.move(move)
            best_move_eval = Math.max(best_move_eval, minimax(depth - 1, game, alpha, beta, !player_sided))
            game.undo()

            alpha = Math.max(alpha, best_move_eval)
            if (beta <= alpha) return best_move_eval
        }

        return best_move_eval
    } else {
        let best_move_eval = 9999

        for (let i = 0; i < game_moves.length; i++)
        {
            let move = game_moves[i]

            game.move(move)
            best_move_eval = Math.min(best_move_eval, minimax(depth - 1, game, alpha, beta, !player_sided))
            game.undo()

            beta = Math.min(beta, best_move_eval)
            if (beta <= alpha) return best_move_eval
        }

        return best_move_eval
    }
}

const make_cheez_move = async (game, player_color) => {

    let start_time = performance.now()
    let best_move = start_minimax(3, game, player_color == "b")
    let time_elapsed = performance.now() - start_time

    $(".stats #time").text("Time Elapsed: " + time_elapsed / 1000 + " seconds")
    $(".stats #positions").text("Positions Evaluated: " + positions_evaluated)
    positions_evaluated = 0

    if (best_move) game.move(best_move)
}